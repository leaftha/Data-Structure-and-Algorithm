// answer 변수를 0으로 초기화합니다.
// n을 7로 나눈 후 Math.ceil() 함수를 사용하여 올림한 값을 answer에 저장합니다.
// 만약 n이 1인 경우, 1을 바로 반환합니다.
// 그 외의 경우에는 answer에 저장된 값을 반환합니다.
// 해당 함수의 목적은 주어진 숫자를 7로 나누고 올림하여 결과를 반환하는 것입니다. 예를 들어, solution(10)을 호출하면 10을 7로 나눈 후 올림한 값인 2를 반환합니다.

