// answer라는 빈 배열을 생성합니다. 이 배열에 홀수들을 담을 것입니다.
// 0부터 n까지 반복문을 돌면서, 각 숫자가 홀수인지 검사합니다.
// 홀수인 경우 해당 숫자를 answer 배열에 추가(push)합니다.
// 반복문이 끝나면 answer 배열을 반환합니다.
// 예를 들어, solution(10)을 호출하면 0부터 10까지의 홀수들인 [1, 3, 5, 7, 9]를 반환합니다.

// 위 함수의 동작은 올바르며, 배열을 이용하여 주어진 조건을 만족하는 값들을 수집하는 간단한 방법입니다.
